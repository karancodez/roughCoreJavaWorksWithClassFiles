class multi
{
  void print(){
    for (int i=1;i<=10 ;i++ ) {
      System.out.printf("%d*%d=%d",5,i,5*i).println();

    }
  }

  void print(int tabel){
    for (int i=1;i<=10 ;i++ ) {
      System.out.printf("%d*%d=%d",tabel,i,tabel*i).println();

    }
  }

  void print(int tabel,int from, int to){
    for (int i=from;i<=to ;i++ ) {
      System.out.printf("%d*%d=%d",tabel,i,tabel*i).println();

    }
  }
}
